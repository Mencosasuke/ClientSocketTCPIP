    private void btnConectarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnConectarActionPerformed
        // TODO add your handling code here:
        if(txtUsername.getText().length() > 0){
            nombreUsuario = txtUsername.getText();
            
            client.ChatClient(ipAddress, 1122, nombreUsuario);

            btnConectar.setEnabled(false);

            Thread listen = new Thread() {
                public void run() {
                    client.listen();
                }
            };
            listen.start();

            txtMensaje.requestFocusInWindow();
        }else{
            JOptionPane.showMessageDialog(gui, "Debe ingresar un nombre de usuario.", "Error", JOptionPane.ERROR_MESSAGE);
            txtUsername.requestFocusInWindow();
        }
    }//GEN-LAST:event_btnConectarActionPerformed

    private void btnEnviarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEnviarActionPerformed
        // TODO add your handling code here:
        if(txtMensaje.getText().length() > 0){
            String mensaje = txtMensaje.getText();
            if(mensaje.substring(0, 1).equals("@")){
                // Es un mensaje dirigido a un usuario
                client.sendMessage(txtMensaje.getText(), nombreUsuario, 3, client.getPortClient());
            }else{
                // Es un mensaje broadcast
                client.sendMessage(txtMensaje.getText(), nombreUsuario, 1, client.getPortClient());
            }
            txtMensaje.setText("");
        }
    }//GEN-LAST:event_btnEnviarActionPerformed

